#!/usr/bin/env python3
"""Build a chart-ready ceremonial psychedelic interaction dataset."""

from __future__ import annotations

import csv
import json
from pathlib import Path
from typing import Dict, List, Tuple


def write_csv(path: Path, rows: List[dict], fieldnames: List[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def write_json(path: Path, payload: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2, ensure_ascii=True)
        f.write("\n")


def sql_quote(value: str) -> str:
    return "'" + value.replace("'", "''") + "'"


def write_sql(path: Path, entities: List[dict], legend: List[dict], interactions: List[dict]) -> None:
    lines: List[str] = []
    lines.append("-- Ceremonial psychedelic interaction dataset seed")
    lines.append("-- Generated by scripts/build_ceremonial_psychedelic_dataset.py")
    lines.append("")
    lines.append("BEGIN;")
    lines.append("")
    lines.append("CREATE TABLE IF NOT EXISTS ceremonial_entities (")
    lines.append("  entity_id TEXT PRIMARY KEY,")
    lines.append("  display_name TEXT NOT NULL,")
    lines.append("  entity_group TEXT NOT NULL,")
    lines.append("  mechanism_tag TEXT NOT NULL,")
    lines.append("  notes TEXT NOT NULL")
    lines.append(");")
    lines.append("")
    lines.append("CREATE TABLE IF NOT EXISTS ceremonial_risk_legend (")
    lines.append("  risk_scale INTEGER PRIMARY KEY,")
    lines.append("  risk_code TEXT NOT NULL,")
    lines.append("  risk_label TEXT NOT NULL,")
    lines.append("  canonical_hex TEXT NOT NULL,")
    lines.append("  description TEXT NOT NULL")
    lines.append(");")
    lines.append("")
    lines.append("CREATE TABLE IF NOT EXISTS ceremonial_interactions (")
    lines.append("  entity_a TEXT NOT NULL REFERENCES ceremonial_entities(entity_id),")
    lines.append("  entity_b TEXT NOT NULL REFERENCES ceremonial_entities(entity_id),")
    lines.append("  risk_scale INTEGER NOT NULL REFERENCES ceremonial_risk_legend(risk_scale),")
    lines.append("  risk_code TEXT NOT NULL,")
    lines.append("  interaction_summary TEXT NOT NULL,")
    lines.append("  evidence_confidence TEXT NOT NULL,")
    lines.append("  source_refs TEXT NOT NULL,")
    lines.append("  PRIMARY KEY (entity_a, entity_b)")
    lines.append(");")
    lines.append("")
    lines.append("DELETE FROM ceremonial_interactions;")
    lines.append("DELETE FROM ceremonial_entities;")
    lines.append("DELETE FROM ceremonial_risk_legend;")
    lines.append("")

    lines.append("INSERT INTO ceremonial_entities (entity_id, display_name, entity_group, mechanism_tag, notes) VALUES")
    for i, row in enumerate(entities):
        suffix = "," if i < len(entities) - 1 else ";"
        lines.append(
            "  ("
            + ", ".join(
                [
                    sql_quote(row["entity_id"]),
                    sql_quote(row["display_name"]),
                    sql_quote(row["entity_group"]),
                    sql_quote(row["mechanism_tag"]),
                    sql_quote(row["notes"]),
                ]
            )
            + f"){suffix}"
        )
    lines.append("")

    lines.append("INSERT INTO ceremonial_risk_legend (risk_scale, risk_code, risk_label, canonical_hex, description) VALUES")
    for i, row in enumerate(legend):
        suffix = "," if i < len(legend) - 1 else ";"
        lines.append(
            "  ("
            + ", ".join(
                [
                    str(row["risk_scale"]),
                    sql_quote(row["risk_code"]),
                    sql_quote(row["risk_label"]),
                    sql_quote(row["canonical_hex"]),
                    sql_quote(row["description"]),
                ]
            )
            + f"){suffix}"
        )
    lines.append("")

    lines.append("INSERT INTO ceremonial_interactions (entity_a, entity_b, risk_scale, risk_code, interaction_summary, evidence_confidence, source_refs) VALUES")
    for i, row in enumerate(interactions):
        suffix = "," if i < len(interactions) - 1 else ";"
        lines.append(
            "  ("
            + ", ".join(
                [
                    sql_quote(row["entity_a"]),
                    sql_quote(row["entity_b"]),
                    str(row["risk_scale"]),
                    sql_quote(row["risk_code"]),
                    sql_quote(row["interaction_summary"]),
                    sql_quote(row["evidence_confidence"]),
                    sql_quote(row["source_refs"]),
                ]
            )
            + f"){suffix}"
        )
    lines.append("")
    lines.append("COMMIT;")
    lines.append("")

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines), encoding="utf-8")


